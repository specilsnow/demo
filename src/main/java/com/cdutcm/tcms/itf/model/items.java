package com.cdutcm.tcms.itf.model;

public class items {
	
	private String m_order;
	
	private String xtph;
	
	private String name;
	
	private String dosage;
	
	private String unit;
	
	private String usage_name;
	
	private String jczs;

	
	public String getM_order() {
		return m_order;
	}

	public void setM_order(String m_order) {
		this.m_order = m_order;
	}

	public String getXtph() {
		return xtph;
	}

	public void setXtph(String xtph) {
		this.xtph = xtph;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDosage() {
		return dosage;
	}

	public void setDosage(String dosage) {
		this.dosage = dosage;
	}

	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getUsage_name() {
		return usage_name;
	}

	public void setUsage_name(String usage_name) {
		this.usage_name = usage_name;
	}

	public String getJczs() {
		return jczs;
	}

	public void setJczs(String jczs) {
		this.jczs = jczs;
	}

	
}
