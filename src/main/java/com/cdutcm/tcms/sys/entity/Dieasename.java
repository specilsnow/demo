package com.cdutcm.tcms.sys.entity;

public class Dieasename {

	
	private int id;
	
	private String name;
	
	private String nickname;
	
	private String westname;
	
	private String code;
	
	private String zyfl;
	
	private String pinyin;
	
	private String ctype;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getWestname() {
		return westname;
	}

	public void setWestname(String westname) {
		this.westname = westname;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getZyfl() {
		return zyfl;
	}

	public void setZyfl(String zyfl) {
		this.zyfl = zyfl;
	}

	public String getPinyin() {
		return pinyin;
	}

	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}

	public String getCtype() {
		return ctype;
	}

	public void setCtype(String ctype) {
		this.ctype = ctype;
	}
}
